let a = 10;
console.log(a > 5); //comparadores relacionales
console.log(a >= 5);
console.log(a <= 5);
console.log(a < 5);

console.log(a == 10); //comparadores de igualdad
console.log(a != 10); //evaluar si son diferentes
console.log(a === '10'); //triple igualdad consulta si tiene el mismo valor y mismo tipo de dato
console.log(a !== '10'); //si son distinto tipo de dato y de valor
